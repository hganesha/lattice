const ICON_BY_ID = new Map(ENTITY_ICONS.map((option) => [option.id, option]))

/** Fallback icon key used when none is chosen (imported types, blank starters). */
export const DEFAULT_ENTITY_ICON = 'box'

export function isKnownEntityIcon(icon: string): boolean {
  return ICON_BY_ID.has(icon)
}

interface EntityIconProps extends IconProps {
  /** Persisted icon key. Unknown values fall back to a text glyph for legacy 2-letter codes. */
  icon: string
}

/**
 * Renders an entity-type icon by key. Legacy contracts stored a 1–2 char text
 * code in `icon`; those render as a monospace glyph so nothing breaks before a
 * user re-picks a real icon.
 */
export function EntityIcon({ icon, ...props }: EntityIconProps) {
  const match = ICON_BY_ID.get(icon)
  if (match) return <match.Icon {...props} />
  return (
    <span className="entity-icon-text" aria-hidden="true" {...(props as SVGProps<HTMLSpanElement>)}>
      {icon}
    </span>
  )
}
